package com.etm.racc.map.lattice;

public class RunResult {
    /**
     * 布尔变量
     */
    private boolean booleanValue;

    public RunResult() {
    }

    public RunResult(boolean booleanValue) {
        this.booleanValue = booleanValue;
    }

    public boolean isBooleanValue() {
        return booleanValue;
    }

}
